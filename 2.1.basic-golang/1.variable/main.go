package main

import "fmt"

func main() {
	// var <nama variable> <type data>
	var name string = "Eko Sugeng Cahyadi"
	// cara lain menulis variable tidak langsung
	var address string
	address = "Sampit"
	// cara lain menggunakan var dengan :=
	age := 22

	fmt.Println(name)
	fmt.Println(address)
	fmt.Println(age)

}
